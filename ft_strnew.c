#include "libft.h"
#include <stdlib.h>

char	*ft_strnew(size_t size)
{
	char *str;

	if (!(str = (char*)malloc(sizeof(*str) * size + 1)))
		return (NULL);
	ft_memset(str, '\0', size + 1);
	return (str);
}
