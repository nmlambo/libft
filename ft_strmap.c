#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char			*str;
	unsigned int	i;

	if (!s || !f)
		return (NULL);
	i = 0;
	str = ft_strdup(s);
	if (!str)
		return (NULL);
	while (*s)
	{
		str[i] = f(*s++);
		i++;
	}
	str[i] = '\0';
	return (str);
}
